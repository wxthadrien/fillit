#include "verif.h"

int		ft_vali_tab(char *line)
{
	int	i;

	i = 0;
	if (ft_strlen(line) != 4)
		return (-1);
	while (i < 4)
	{
		if (line[i] != '.' && line[i] != '#')
			return (-1);
		i++;
	}
	return (0);
}
